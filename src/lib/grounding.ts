// 学習済みパターンから例を生成（中央総合の正解例も常に含める）
export function buildFewShotExamples(
  pastDecisions: CategorizedExample[],
  industry: string,
  limit: number = 15
): string {
  // 中央総合の役員借入金パターンは常に含める（最も重要な税務リスク回避）
  const goldExamples = CHUO_SOGO_GOLD_EXAMPLES;
  const userExamples = pastDecisions.slice(0, Math.max(0, limit - goldExamples.length));
  const combined = [...goldExamples, ...userExamples];

  return '## 正解付き実例（特に役員立替・個人口座のパターンに注意）\n' +
    combined.map((ex, i) =>
      `${i + 1}. ${ex.type === 'income' ? '入金' : '出金'} ¥${ex.amount.toLocaleString()}「${ex.description}」→ ${ex.category}（${ex.reason}）`
    ).join('\n');
}

